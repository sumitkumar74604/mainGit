# Variable: It is a reserved memory location to store value.
'''
In Python, variable refers to a reference variable, when an value is assign to a variable, it store memory address of that value. 
'''
x=10
print("Type of:",type(x))
print("x is:",x,"Memory Address:",id(x))
y=20
print("y is:",y,"Memory Address:",id(y))
z=10
print("z is:",z,"Memory Address:",id(z))
name = "Videh Jaiswal"
print("Type of:",type(name))
print("Name is:",name,"Memory Address:",id(name))

salary = 34567.45
print("Type of:",type(salary))
print("Salary is:",salary,"Memory Address:",id(salary))